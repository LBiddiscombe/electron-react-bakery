let products = []
categories.forEach(cat => {
   const datacat = data.Categories.Category.find(c => c.name === cat.label)
   datacat.product.forEach(p => {
      products.push({
         id: ++i,
         label: p.name,
         image: p.image,
         parent: cat.id,
         type: 'product',
         labelData: {
            title: p.DescriptionBold,
            allergens: p.Description,
            price: p.price,
            life: p.ShelfLife,
            barcode: p.barcode
         }
      })
   })
})

const items = categories.concat(products)

